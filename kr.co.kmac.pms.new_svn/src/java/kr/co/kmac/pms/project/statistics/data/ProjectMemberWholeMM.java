package kr.co.kmac.pms.project.statistics.data;

public class ProjectMemberWholeMM {

	private String ssn;
	private String name;
	private String projectCode;
	private String projectName;
	private String businessTypeName;
	private String sanctionDate;
	private String realStartDate;
	private String realEndDate;
	private String runningDeptName;
	private String totalMM;
	
	public String getSsn() {
		return ssn;
	}
	public void setSsn(String ssn) {
		this.ssn = ssn;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getProjectCode() {
		return projectCode;
	}
	public void setProjectCode(String projectCode) {
		this.projectCode = projectCode;
	}
	public String getProjectName() {
		return projectName;
	}
	public void setProjectName(String projectName) {
		this.projectName = projectName;
	}
	public String getBusinessTypeName() {
		return businessTypeName;
	}
	public void setBusinessTypeName(String businessTypeName) {
		this.businessTypeName = businessTypeName;
	}
	public String getSanctionDate() {
		return sanctionDate;
	}
	public void setSanctionDate(String sanctionDate) {
		this.sanctionDate = sanctionDate;
	}
	public String getRealStartDate() {
		return realStartDate;
	}
	public void setRealStartDate(String realStartDate) {
		this.realStartDate = realStartDate;
	}
	public String getRealEndDate() {
		return realEndDate;
	}
	public void setRealEndDate(String realEndDate) {
		this.realEndDate = realEndDate;
	}
	public String getRunningDeptName() {
		return runningDeptName;
	}
	public void setRunningDeptName(String runningDeptName) {
		this.runningDeptName = runningDeptName;
	}
	public String getTotalMM() {
		return totalMM;
	}
	public void setTotalMM(String totalMM) {
		this.totalMM = totalMM;
	}
}