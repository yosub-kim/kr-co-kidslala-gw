package kr.co.kmac.pms.schedule.data;

public class CustomerPickerInfo {
	private String idx;
	private String pickerSsn;
	private String infoDate;
	private String year;
	private String month;
	private String day;
	private String subject;
	private String customerName;
	private String customerWorkName;
	
	public String getIdx() {
		return idx;
	}
	public void setIdx(String idx) {
		this.idx = idx;
	}
	public String getPickerSsn() {
		return pickerSsn;
	}
	public void setPickerSsn(String pickerSsn) {
		this.pickerSsn = pickerSsn;
	}
	public String getInfoDate() {
		return infoDate;
	}
	public void setInfoDate(String infoDate) {
		this.infoDate = infoDate;
	}
	public String getYear() {
		return year;
	}
	public void setYear(String year) {
		this.year = year;
	}
	public String getMonth() {
		return month;
	}
	public void setMonth(String month) {
		this.month = month;
	}
	public String getDay() {
		return day;
	}
	public void setDay(String day) {
		this.day = day;
	}
	public String getSubject() {
		return subject;
	}
	public void setSubject(String subject) {
		this.subject = subject;
	}
	public String getCustomerName() {
		return customerName;
	}
	public void setCustomerName(String customerName) {
		this.customerName = customerName;
	}
	public String getCustomerWorkName() {
		return customerWorkName;
	}
	public void setCustomerWorkName(String customerWorkName) {
		this.customerWorkName = customerWorkName;
	}
}