	private String getIconType(int val) {
		if (val == 0) {
			return "";
		}
		if (val >= 61111) {
			// System.out.println("up-day : "+ val);
			return "54321-2.png";// Up-day
		} else if (val < 0 && val > -7) {
			// System.out.println("휴가 : "+ val);
			return "99.png";// 휴가
		} else if (val == -12) {
			// System.out.println("개인휴무 : "+ val);
			return "100.png";// 개인휴무(전문가그룹)
		} else if (val == -56) {
			// System.out.println("반차(고객+휴가) : "+ val);
			return "102.png";// 개인휴무(전문가그룹)
		} else if (val == -5598) {
			// System.out.println("반차(고객+휴가) : "+ val);
			return "105.png";// 개인휴무(전문가그룹)
		} else if (val == -5998) {
			// System.out.println("반차(내부+휴가) : "+ val);
			return "104.png";// 개인휴무(전문가그룹)
		} else if (val == -5968) {
			// System.out.println("반차(내부+휴가) : "+ val);
			return "106.png";// 개인휴무(전문가그룹)
		} else if (val == -597) {
			// System.out.println("반차(외부+휴가) : "+ val);
			return "99.png";// 개인휴무(전문가그룹)
		} else if (val < -5998) {
			// System.out.println("반차(외부+휴가) : "+ val);
			return "101.png";// 개인휴무(전문가그룹)
		} else if (51000 <= val && val < 61111) {
			// System.out.println("up-day : "+ val);
			return "98.png";// 교육
		} else {
			return val + ".png";
		}
		/*
		 * 내부+외부 21 내부+프로젝트 31 외부+고객정보 32 내부+고객정보 41 외부+고객정보 42 프로젝트+고객정보 43 내부+외부+프로젝트 321 내부+외부+고객정보 421 외부+프로젝트+고객정보 431 내부+외부+프로젝트+고객정보 4321 고객+휴가 -56 내부+휴가
		 * -59999 외부+휴가 -5998 프로젝트+내부+휴가 -5968
		 */
	}

