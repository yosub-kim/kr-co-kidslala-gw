package kr.co.kmac.pms.orgdb.data;

public class OrgdbDetailContactPoint {
	private String orgCode;
	private String contactName;
	private String contactPosition;
	private String contactWork;
	private String contactTel;
	private String contactMobile;
	private String contactEmail;
	/**
	 * @return Returns the orgCode.
	 */
	public String getOrgCode() {
		return orgCode;
	}
	/**
	 * @param orgCode The orgCode to set.
	 */
	public void setOrgCode(String orgCode) {
		this.orgCode = orgCode;
	}
	/**
	 * @return Returns the contactName.
	 */
	public String getContactName() {
		return contactName;
	}
	/**
	 * @param contactName The contactName to set.
	 */
	public void setContactName(String contactName) {
		this.contactName = contactName;
	}
	/**
	 * @return Returns the contactPosition.
	 */
	public String getContactPosition() {
		return contactPosition;
	}
	/**
	 * @param contactPosition The contactPosition to set.
	 */
	public void setContactPosition(String contactPosition) {
		this.contactPosition = contactPosition;
	}
	/**
	 * @return Returns the contactWork.
	 */
	public String getContactWork() {
		return contactWork;
	}
	/**
	 * @param contactWork The contactWork to set.
	 */
	public void setContactWork(String contactWork) {
		this.contactWork = contactWork;
	}
	/**
	 * @return Returns the contactTel.
	 */
	public String getContactTel() {
		return contactTel;
	}
	/**
	 * @param contactTel The contactTel to set.
	 */
	public void setContactTel(String contactTel) {
		this.contactTel = contactTel;
	}
	/**
	 * @return Returns the contactMobile.
	 */
	public String getContactMobile() {
		return contactMobile;
	}
	/**
	 * @param contactMobile The contactMobile to set.
	 */
	public void setContactMobile(String contactMobile) {
		this.contactMobile = contactMobile;
	}
	/**
	 * @return Returns the contactEmail.
	 */
	public String getContactEmail() {
		return contactEmail;
	}
	/**
	 * @param contactEmail The contactEmail to set.
	 */
	public void setContactEmail(String contactEmail) {
		this.contactEmail = contactEmail;
	}

}
